input = int(input("Enter the day number: "))

if (input == 1):
    print("Monday")
elif (input == 2):
    print("Tuesday")
elif (input == 3):
    print("Wednesday")
elif (input == 4):
    print("Thursday")
elif (input == 5):
    print("Friday")
elif (input == 6):
    print("Saturday")
elif (input == 7):
    print("Sunday")
else:
    print("Please enter a number between 1 - 7!")